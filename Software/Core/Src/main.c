/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */
	//Set initial Target Voltage
	targetVout = 5000;
	//Set initial conversion mode
	conversionState = CONVERSION_STATE_SHUTDOWN;
  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */
	LL_HRTIM_StartDLLCalibration(HRTIM1);
  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_HRTIM1_Init();
  MX_ADC1_Init();
  MX_FDCAN2_Init();
  MX_I2C1_Init();
  MX_I2C2_Init();
  MX_SPI3_Init();
  MX_UART5_Init();
  MX_USB_Device_Init();
  MX_CRC_Init();
  /* USER CODE BEGIN 2 */
	LL_ADC_DisableDeepPowerDown(ADC1);
	HAL_Delay(1);	//Wait before powering up the regulator
	LL_ADC_EnableInternalRegulator(ADC1);
	HAL_Delay(1);	//Wait for regulator stabilization
	LL_ADC_StartCalibration(ADC1, LL_ADC_SINGLE_ENDED);
	while (LL_ADC_IsCalibrationOnGoing(ADC1) != 0);
	HAL_Delay(1);	//Wait after calibration before activation ADC
	LL_ADC_Enable(ADC1);
	while (LL_ADC_IsActiveFlag_ADRDY(ADC1) == 0);
	HAL_Delay(1);	//Wait before starting conversion
	//Start Timer and ADC Peripherals
	LL_ADC_INJ_StartConversion(ADC1);
	LL_HRTIM_TIM_CounterEnable(HRTIM1, LL_HRTIM_TIMER_E | LL_HRTIM_TIMER_F);
	//Init ADC Filters
	Moving_Average_Init(&VinFilter);
	Moving_Average_Init(&VoutFilter);
	Moving_Average_Init(&CurInFilter);
	Moving_Average_Init(&CurOutFilter);
	//Enable Interrupts
	LL_HRTIM_EnableIT_REP(HRTIM1, LL_HRTIM_TIMER_F);
	LL_ADC_EnableIT_JEOS(ADC1);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	char txBuf[40];
	uint8_t USBCmd[9];
	uint32_t CMDLen;
	while (1)
	{
		if (USB_retrieveCMD(USBCmd, &CMDLen))
		{
			if (CMDLen == 3)
			{
				switch (USBCmd[0])
				{
				case 'O':
					targetVout = (((USBCmd[1] - '0') * 10) + (USBCmd[2] - '0')) * 1000;
					break;
				}
			}
		}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
		sprintf(txBuf, "%u,%u,%u,%u,%u\r\n", VinAverage, VoutAverage, CurInAverage, CurOutAverage, CurrentDuty);
		CDC_Transmit_FS((uint8_t*) txBuf, strlen(txBuf));
		HAL_Delay(1);
	}
  /* USER CODE END 3 */
}

